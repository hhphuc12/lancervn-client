// @flow weak
import {
    REQUEST_SELECT_SKILL,
    RECEIVED_SELECT_SKILL,
    ERROR_SELECT_SKILL,
} from "../constants/skillType";
import moment from "moment/moment";

const initialState = {
    skills: [],
    isFetching: false,
};

const currentTime = moment().format();

export default function (
    state = initialState,
    action
) {
    switch (action.type) {
        case REQUEST_SELECT_SKILL:
            return {
                ...state,
                actionTime: action && action.time ?  action && action.time : currentTime,
                isFetching:  action && action.isFetching ?  action && action.isFetching : initialState.isFetching
            };

        case RECEIVED_SELECT_SKILL:
            return {
                ...state,
                actionTime: action && action.time ?  action && action.time : currentTime,
                isFetching: action && action.isFetching ?  action && action.isFetching : initialState.isFetching,
                skills: action && action.skills ? action && action.skills : initialState.skills,
            };

        case ERROR_SELECT_SKILL:
            return {
                ...state,
                actionTime: action && action.time ?  action && action.time : currentTime,
                isFetching:  action && action.isFetching ?  action && action.isFetching : initialState.isFetching,
            };

        default:
            return { ...state };
    }
}
